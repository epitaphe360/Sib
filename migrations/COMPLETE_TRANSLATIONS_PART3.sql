  content_en = $$<h2>Container and Cargo Terms</h2>

<h3>TEU (Twenty-foot Equivalent Unit)</h3>
<p>Standard measurement unit for container capacity. One TEU = one 20-foot container (20' x 8' x 8.5'). A 40-foot container = 2 TEU. Example: "Tanger Med handles 9+ million TEU annually."</p>

<h3>FEU (Forty-foot Equivalent Unit)</h3>
<p>One 40-foot container. Less commonly used than TEU but important for freight rates. Most modern containers are 40-foot (high cube: 40' x 8' x 9.5').</p>

<h3>Break Bulk</h3>
<p>Cargo shipped in individual pieces (not containerized). Examples: machinery, steel coils, lumber. Requires specialized terminal equipment and labor-intensive handling.</p>

<h3>Bulk Cargo</h3>
<p>Loose cargo poured directly into ship holds. <strong>Dry bulk</strong>: Coal, grain, ore, cement. <strong>Liquid bulk</strong>: Oil, chemicals, LNG.</p>

<h3>Ro-Ro (Roll-on/Roll-off)</h3>
<p>Cargo driven on/off ships (vehicles, trucks, trailers). Specialized vessels with ramps. Major ro-ro ports: Tanger Med, Zeebrugge, Bremerhaven.</p>

<h2>Vessel Types and Terminology</h2>

<h3>Container Ship</h3>
<p>Vessel designed exclusively for container transport. Sizes: Feeder (1,000-3,000 TEU), Panamax (4,000-5,000 TEU), Post-Panamax (6,000-10,000 TEU), Ultra-Large (18,000-24,000 TEU).</p>

<h3>Feeder Vessel</h3>
<p>Small container ship redistributing cargo from major hubs to regional ports. Typically 1,000-3,000 TEU capacity. Essential for hub-and-spoke systems.</p>

<h3>Draft</h3>
<p>Vertical distance between waterline and lowest point of vessel (keel). Determines required channel depth. Mega-ships: 15-16m draft; Port depth must exceed draft plus safety margin. Dredging deepens channels to accommodate larger vessels.</p>

<h3>Berth</h3>
<p>Designated spot where vessel docks. Includes quay wall, bollards (mooring posts), fenders (cushioning). "Berth availability" critical for vessel scheduling.</p>

<h3>Dwt (Deadweight Tonnage)</h3>
<p>Total weight a ship can carry (cargo + fuel + water + supplies). Different from ship's own weight. Example: VLCC (Very Large Crude Carrier) = 200,000-320,000 dwt.</p>

<h2>Port Operations Terms</h2>

<h3>Transshipment</h3>
<p>Transferring cargo between vessels without entering national customs territory. Tanger Med: 70% transshipment. Gateway cargo enters customs; transshipment cargo stays international.</p>

<h3>Dwell Time</h3>
<p>Duration container remains in port/terminal. Long dwell time = congestion, storage costs. Efficient ports: 3-5 days average.</p>

<h3>Vessel Turnaround Time</h3>
<p>Time from vessel arrival to departure. Includes berthing, cargo operations, deberthing. Modern terminals: 24-36 hours for 10,000 TEU vessel. Critical efficiency metric.</p>

<h3>STS Crane (Ship-to-Shore)</h3>
<p>Massive cranes loading/unloading containers from vessels. Mega-ship cranes: 65m+ outreach, 140m+ height, $10-15M cost each. Moves: 25-40 containers/hour per crane.</p>

<h3>ASC (Automated Stacking Crane)</h3>
<p>Remote-controlled cranes organizing containers in storage yards. Rail-mounted, stacking 5-7 containers high. Used in automated terminals.</p>

<h3>AGV (Automated Guided Vehicle)</h3>
<p>Driverless electric vehicles transporting containers within terminals. Following magnetic guides or GPS. Used in Rotterdam, Singapore, Shanghai automated ports.</p>

<h3>Yard Utilization</h3>
<p>Percentage of container yard capacity in use. High utilization (>80%) = efficiency but risks congestion. Optimal: 65-75% allowing operational flexibility.</p>

<h2>Maritime Trade and Logistics</h2>

<h3>Bill of Lading (B/L)</h3>
<p>Legal document serving as: (1) cargo receipt, (2) transport contract, (3) title document. Types: Master B/L (carrier to freight forwarder), House B/L (forwarder to shipper).</p>

<h3>Incoterms</h3>
<p>International Commercial Terms defining buyer/seller responsibilities. Common: <strong>FOB</strong> (Free on Board - seller loads, buyer pays shipping), <strong>CIF</strong> (Cost, Insurance, Freight - seller pays shipping, buyer takes over at destination port), <strong>EXW</strong> (Ex Works - buyer arranges everything).</p>

<h3>Demurrage</h3>
<p>Penalty fee for exceeding allowed time to load/unload vessel or return containers. Expensive: $100-300/day per container. Incentivizes fast cargo pickup.</p>

<h3>Detention</h3>
<p>Fee for keeping container beyond allowed period outside port. Different from demurrage (inside port). Both incentivize equipment return.</p>

<h3>Cabotage</h3>
<p>Shipping between ports of same country or region. Often restricted to domestic carriers. Example: Morocco limiting cabotage to Moroccan-flagged vessels.</p>

<h2>Port Infrastructure Terms</h2>

<h3>Quay/Wharf</h3>
<p>Platform where vessels dock. Built alongshore (quay) or perpendicular (pier/wharf). Must support heavy cranes and container stacks.</p>

<h3>Breakwater</h3>
<p>Offshore structure protecting port from waves. Massive investment: Hundreds of millions for major ports. Example: Tanger Med 3.5km breakwater.</p>

<h3>Apron</h3>
<p>Paved area immediately behind quay where cargo handling occurs. Width critical for crane rails, truck lanes, temporary container storage.</p>

<h3>Channel</h3>
<p>Dredged waterway allowing vessels to enter port. Marked by buoys. Maintained depth critical; siltation requires regular dredging.</p>

<h3>Dry Port/Inland Terminal</h3>
<p>Inland facility with customs clearance capabilities. Extends port reach inland. Example: Zenata (Casablanca) reducing port congestion.</p>

<h2>Customs and Regulatory</h2>

<h3>Port State Control (PSC)</h3>
<p>Inspections ensuring vessels meet international safety/environmental standards. Poor PSC results = vessel detained. Regional agreements (Paris MoU, Tokyo MoU).</p>

<h3>ISPS Code (International Ship and Port Facility Security)</h3>
<p>Security standards implemented after 9/11. Requires security assessments, plans, officers. Port access  control, surveillance, screening.</p>

<h3>AIS (Automatic Identification System)</h3>
<p>Transponders broadcasting vessel position, course, speed. Used for traffic management, collision avoidance, security monitoring.</p>

<h3>VTS (Vessel Traffic Service)</h3>
<p>Shore-based system monitoring vessel movements. Like air traffic control for ships. Mandatory in congested waters.</p>

<h2>Commercial and Financial</h2>

<h3>Port Tariff</h3>
<p>Fees for port services: vessel dues (based on size), cargo handling, storage, pilotage, towage. Transparent tariffs attract customers; monopolistic pricing drives cargo away.</p>

<h3>Concession</h3>
<p>Long-term agreement (typically 25-35 years) granting private operator rights to build/operate terminal. Operator invests capital; port authority collects rent/concession fees.</p>

<h3>Free Trade Zone (FTZ)</h3>

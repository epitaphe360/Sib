import re

def escape_value(val):
    """Échappe les apostrophes pour les strings TS."""
    return val.replace("\\", "\\\\").replace("'", "\\'")

def inject_keys(filepath, new_keys):
    with open(filepath, encoding='utf-8') as f:
        content = f.read()
    
    lines_to_add = []
    for key, val in new_keys.items():
        escaped = escape_value(val)
        lines_to_add.append(f"    '{key}': '{escaped}',")
    
    # Insérer avant la dernière ligne (fermeture `};`)
    insert_text = '\n'.join(lines_to_add)
    # Trouver la dernière accolade
    last_brace = content.rfind('};')
    if last_brace == -1:
        print(f"ERROR: cannot find closing brace in {filepath}")
        return False
    
    new_content = content[:last_brace] + insert_text + '\n' + content[last_brace:]
    with open(filepath, 'w', encoding='utf-8') as f:
        f.write(new_content)
    return True

def fix_stubs(filepath, fixes):
    """Remplace les valeurs vides par les traductions correctes."""
    with open(filepath, encoding='utf-8') as f:
        content = f.read()
    
    for key, val in fixes.items():
        escaped = escape_value(val)
        # Pattern: 'key': '' ou 'key': "  "
        pattern = r"(    '" + re.escape(key) + r"'\s*:\s*)['\"](\s*|TODO|\.\.\.)['\"]"
        replacement = f"\\1'{escaped}'"
        content, count = re.subn(pattern, replacement, content)
        if count == 0:
            print(f"  WARN: stub key not found: {key}")
    
    with open(filepath, 'w', encoding='utf-8') as f:
        f.write(content)

